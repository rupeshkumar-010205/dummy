        self.c2f_2=C2f(64, 64, 2, True)
        self.cv_4=Conv(64, 128, 3, 2)
        self.c2f_3=C2f(128, 128, 2, True)
        self.cv_5=Conv(128, 256, 3, 2)
        self.c2f_4=C2f(256, 256, 1, True)
        self.SPP=SPPF(256, 256, 5)
