class BackBone(nn.Module):
    def __init__(self, backbone_name='resnet50', weights=ResNet50_Weights.DEFAULT, trainable_layers=0):
        super(BackBone, self).__init__()
        self.backbone = resnet_fpn_backbone(backbone_name=backbone_name, weights=weights, trainable_layers=trainable_layers)
    
    def forward(self, x):
        output = self.backbone(x)

        return output["1"],output["2"],output["3"]

class FPN(nn.Module):
    def __init__(self):
        super().__init__()
        self.upsample_1=nn.Upsample(None, 2, 'nearest')
        self.c2f_5=C2f(512, 128, 1)
        self.upsample_2=nn.Upsample(None, 2, 'nearest')
        self.c2f_6=C2f(384, 64, 1)
        self.cv_6=Conv(64, 64, 3, 2)
        self.c2f_7=C2f(192, 128, 1)
        self.cv_7=Conv(128, 128, 3, 2)
        self.c2f_8=C2f(384, 256, 1)
    def forward(self,layers):
        p3,p5,p8=layers
        p81=p8
        p51=self.c2f_5(torch.cat([self.upsample_1(p8),p5],dim=1))
        out1=self.c2f_6(torch.cat([self.upsample_2(p51),p3],dim=1))
        out2=self.c2f_7(torch.cat([self.cv_6(out1),p51],dim=1))
        out3=self.c2f_8(torch.cat([self.cv_7(out2),p81],dim=1))
        return [out1,out2,out3]

class CustomDetection(nn.Module):
    def __init__(self,nc=80):
        super().__init__()
        self.Backbone=BackBone()
        self.FPN=FPN()
        img_dummy=torch.zeros((1,3,640,640))
        ch=[x.shape[-3] for x in self.FPN(self.Backbone(img_dummy))]
        self.detect=Detect(nc=nc,ch=ch)
        m=self.detect
        s = 256  # 2x min stride
        m.inplace = True
        m.stride = torch.tensor([s / x.shape[-2] for x in self.forward(torch.zeros((1, 3, s, s)))])  # forward
        self.stride = m.stride
        m.bias_init()  # only run once
        initialize_weights(self)
        model_info(self,detailed=True)
    def forward(self, x, *args, **kwargs):
        """
        Forward pass of the model on a single scale. Wrapper for `_forward_once` method.

        Args:
            x (torch.Tensor | dict): The input image tensor or a dict including image tensor and gt labels.

        Returns:
            (torch.Tensor): The output of the network.
        """
        if isinstance(x, dict):  # for cases of training and validating while training.
            return self.loss(x, *args, **kwargs)
        return self.predict(x, *args, **kwargs)
    def _apply(self, fn):
        """
        Applies a function to all the tensors in the model that are not parameters or registered buffers.

        Args:
            fn (function): the function to apply to the model

        Returns:
            (BaseModel): An updated BaseModel object.
        """
        self = super()._apply(fn)
        m = self.detect
        if isinstance(m, Detect):  # includes all Detect subclasses like Segment, Pose, OBB, WorldDetect
            m.stride = fn(m.stride)
            m.anchors = fn(m.anchors)
            m.strides = fn(m.strides)
        return self

    def load(self, weights, verbose=True):
        """
        Load the weights into the model.

        Args:
            weights (dict | torch.nn.Module): The pre-trained weights to be loaded.
            verbose (bool, optional): Whether to log the transfer progress. Defaults to True.
        """
        model = weights["model"] if isinstance(weights, dict) else weights  # torchvision models are not dicts
        csd = model.float().state_dict()  # checkpoint state_dict as FP32
        csd = intersect_dicts(csd, self.state_dict())  # intersect
        self.load_state_dict(csd, strict=False)  # load
        if verbose:
            LOGGER.info(f"Transferred {len(csd)}/{len(self.state_dict())} items from pretrained weights")


    def predict(self,x):
        return self.detect(self.FPN(self.Backbone(x)))
    
    
    def init_criterion(self):
        """Initialize the loss criterion for the DetectionModel."""
        return v8DetectionLoss(self)

    def loss(self, batch, preds=None):
        """
        Compute loss.

        Args:
            batch (dict): Batch to compute loss on
            preds (torch.Tensor | List[torch.Tensor]): Predictions.
        """
        if not hasattr(self, "criterion"):
            self.criterion = self.init_criterion()

        preds = self.forward(batch["img"]) if preds is None else preds
        return self.criterion(preds, batch)


