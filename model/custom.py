        x=self.cv_1(x)
        x=self.cv_2(x)
        x=self.c2f_1(x)
        x=self.cv_3(x)
        x=self.c2f_2(x)
        p3=x
        x=self.cv_4(x)
        x=self.c2f_3(x)
        p5=x
        x=self.cv_5(x)
        x=self.c2f_4(x)
        x=self.SPP(x)
        p8=x
        return (p3,p5,p8)


